struct MiniMaxResult {
	MiniMaxResult() :
		bestLeaveNode(nullptr),
		evaluationValue(0)
	{}

	MiniMaxResult(
		Node* bestLeaveNode,
		int evaluationValue
	) :
		bestLeaveNode(bestLeaveNode),
		evaluationValue(evaluationValue)
	{}

	Node* bestLeaveNode;
	int evaluationValue;
};

